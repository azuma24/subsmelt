function getNested(obj: unknown, dottedKey: string): unknown {
  return dottedKey.split(".").reduce<unknown>((current, segment) => {
    if (typeof current !== "object" || current === null) return undefined;
    return (current as Record<string, unknown>)[segment];
  }, obj);
}

for (const locale of localeDirs) {
  test(`transcription locale keys are present for ${locale}`, () => {
    const file = join(process.cwd(), "src", "client", "locales", locale, "translation.json");
    const data = JSON.parse(readFileSync(file, "utf8"));
    const missing = requiredTranscriptionKeys.filter((key) => typeof getNested(data, key) !== "string");
    assert.deepEqual(missing, []);
  });
}
